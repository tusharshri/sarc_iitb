<li> A minimum of 10 calls will have to be made to complete one slot</li>
<li> The follow up mail will be a part of the call</li>
<li> No time limit will be imposed on a slot</li>
<li> If a call cannot be made and instead an email is sent, then 2 such emails will account to 1 call 
</ul>
<li>By Time</li>
<ul>  
  <li> Every Slot will be of 60 minutes</li>
  <li> Assume that no call takes more than 6 minutes</li>
  <li> A minimum of 10 calls will have to be made within the slot</li>
  <li> What if a volunteer has a good interaction with the alumnus/alumna and the call goes on for more than 6 minutes?</li>
  Special Case:A Call takes more than 6 minutes</li>
  <li> Suppose n calls take more than six minutes.</li>
  <li> Then the volunteer has to call at the rate of 1 call per 6 minutes for the remaining time.</li>
  <li> Let the time for each of these calls be t<sub>i</sub></li>
  <li> Then, at least [(60 - &Sigma; t<sub>i</sub> )/6] calls have to be made in the remaining (60 - &Sigma; t<sub>i</sub> ) minutes</li>
  <li> Example: 2 calls take more than 6 minutes, say 8 and 10. Then, 60 &ndash; 18 = 42 minutes are left. Hence, 42min/6min per call = 7 calls</li>
  <li> Thus total 7 + 2 = 9 calls should be made, at least. Only then that slot is approved as completed</p>
</ul>
