                      <div><strong id="internal-source-marker_0.48161884886212647">Student Alumni Meet (SAM) scheduled in the first weekend of October (Oct 6 - 7) is the flagship event of SARC. SAM is the platform which brings alumni and students together. We invite alumni from different fields to come forward and give interactive buddy talks, participate in panel debates and indulge themselves in other events. Due to its rising popularity, it is already on its way of becoming the most awaited event of the year.</strong></div>
