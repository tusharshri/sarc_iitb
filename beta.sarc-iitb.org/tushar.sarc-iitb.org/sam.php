    </li>
    <li tabindex="1">
      <span class="title">Panel Discussion </span> 
      <div class="content">A panel consisting of alumni, faculty and student representatives is invited to discuss on a student centric topic. Thought provoking debates on some of the most despised, desired and debated topics in the institute today makes this an event not to be missed.</div>
